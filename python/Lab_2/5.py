import math

sin_60_deg = math.sin(math.radians(60))
cos_pi = math.cos(math.pi)
sin_value = math.sin(0.8660254037844386)
tan_90_deg = math.tan(math.radians(90))

print("Sine of 60 degrees is:", sin_60_deg)
print("Cosine of pi is:", cos_pi)
print("Sine of 0.8660254037844386 is:", sin_value)
print("Tangent of 90 degrees is:", tan_90_deg)
